#Tuple with for loop
menu_items = ('fries', 'salad', 'chicken', 'steak', 'gravey')
print("We offer the following items on our menu:")
for menu_item in menu_items:
    print("- " + menu_item)


menu_items = ('mashed potatoes', 'salad', 'chicken', 'fish', 'gravey')
print("\nOur menu has now changed to the following:")
for menu_item in menu_items:
    print("- " + menu_item)
