destinations = ['italy', 'spain', 'greece', 'banff', 'maldives']
print(destinations)

#sorted list
print("\nHere is the sorted list:")
print(sorted(destinations))

#original order
print(destinations)

#reverse alphabetical order
print(sorted(destinations, reverse=True))

#original order
print(destinations)

#reverse order
destinations.reverse()
print(destinations)

#reverse again
destinations.reverse()
print(destinations)

#alphabetical order
destinations.sort()
print(destinations)

#reverse alphabetical order
destinations.sort(reverse=True)
print(destinations)
